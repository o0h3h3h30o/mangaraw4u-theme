export function EmojiPickerComponent({
  onEmojiSelect,
  className,
}: EmojiPickerProps) {
  const t = useTranslations("emojiPicker");
  const [searchTerm, setSearchTerm] = React.useState("");

  const filteredEmojis = React.useMemo(() => {
    if (!searchTerm) return COMMON_EMOJIS;
    return COMMON_EMOJIS.filter((emoji) => emoji.includes(searchTerm));
  }, [searchTerm]);

  return (
    <div className={cn("w-[320px] p-2 space-y-2", className)}>
      <input
        type="text"
        placeholder={t("search")}
        value={searchTerm}
        onChange={(e) => setSearchTerm(e.target.value)}
        className="w-full px-3 py-2 text-sm border rounded-md bg-background focus:outline-none focus:ring-2 focus:ring-ring"
        aria-label={t("search")}
      />
      <div className="h-[200px] overflow-y-auto" role="grid">
        <div className="grid grid-cols-8 gap-1 p-1">
          {filteredEmojis.map((emoji, index) => (
            <button
              key={index}
              onClick={() => onEmojiSelect(emoji)}
              className="flex items-center justify-center w-8 h-8 text-lg rounded hover:bg-accent focus:bg-accent focus:outline-none transition-colors"
              type="button"
              aria-label={`Emoji: ${emoji}`}
              role="gridcell"
            >
              {emoji}
            </button>
          ))}
        </div>
        {filteredEmojis.length === 0 && (
          <div
            className="flex items-center justify-center h-full text-sm text-muted-foreground"
            role="status"
            aria-live="polite"
          >
            {t("noEmojiFound")}
          </div>
        )}
      </div>
    </div>
  );
}
